#include <stdio.h>

int main() {

    int Num_1 = 100;
    int Num_2 = 50;

    printf("Addition of two numbers is: %d\n", Num_1 + Num_2);
    printf("Subtraction of two numbers is: %d\n", Num_1 - Num_2);
    printf("Multiplication of two numbers is: %d\n", Num_1 * Num_2);
    printf("Division of two numbers is: %d\n", Num_1 / Num_2);

    return 0;
}